import { Stack } from "expo-router";

import {
  Poppins_400Regular,
  Poppins_500Medium,
  Poppins_600SemiBold,
  Poppins_700Bold,
  useFonts,
} from "@expo-google-fonts/poppins";

export default function RootLayout() {


      const [fontsLoaded] = useFonts({
      Poppins_400Regular,
      Poppins_500Medium,
      Poppins_600SemiBold,
      Poppins_700Bold,
    });


    if (!fontsLoaded) {
      return null;
    }

    return <Stack
        screenOptions={{
            headerShown: false,
        }}
        />
}
